
public class Employee {
	int empNum;
	double empSalary;
	
	public void setEmpNum(int empNum) {
		this.empNum = empNum;
	}
	public int getEmpNum() {
		return empNum;
	}
	
	public void setEmpSalary(double empSalary) {
		this.empSalary = empSalary;
	}
	public double getEmpSalary() {
		return empSalary;
	}
	

}
